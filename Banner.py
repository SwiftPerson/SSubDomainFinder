def clearWindows():
  os.system("cls")

def clearLinux():
  os.system("clear")



def AutoClear():
  if os.name == 'nt':
    clearWindows()
  else:
    clearLinux()


def slowprint(s,t):
    for c in s + '\n' :
        sys.stdout.write(c)
        sys.stdout.flush() 
        time.sleep(t / 100)
